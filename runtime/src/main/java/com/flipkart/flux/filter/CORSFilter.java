package com.flipkart.flux.filter;

import javax.ws.rs.container.ContainerRequestContext;
import javax.ws.rs.container.ContainerResponseContext;
import javax.ws.rs.container.ContainerResponseFilter;
import javax.ws.rs.core.MultivaluedMap;
import java.io.IOException;

/**
 * <code>CORSFilter</code> is a Jersey filter which modifies inbound and outbound requests and responses,
 * and adds required cross origin resource sharing headers.
 *
 * @author shyam.akirala
 */
public class CORSFilter implements ContainerResponseFilter {

    public void filter(ContainerRequestContext requestContext, ContainerResponseContext responseContext)
            throws IOException {

        MultivaluedMap<String, Object> headers = responseContext.getHeaders();
        headers.add("Access-Control-Allow-Origin", "*");
        headers.add("Access-Control-Allow-Methods", "GET, POST, DELETE, PUT, HEAD");
        headers.add("Access-Control-Allow-Headers", "X-Requested-With, Content-Type, Accept, Origin");
    }

}
